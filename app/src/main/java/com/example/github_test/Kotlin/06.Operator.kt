package com.example.myapplication.Kotlin

// 06. 연산자

// (1) 산술 연산자: +, -, *, /, % (/는 몫만, %는 나머지만 출력. 둘 다 나눗셈)

// (2) 대입 연산자: 좌변 = 우변 (우변 값이 좌변으로 들어가는 것)
// (ex) var num[좌변] = 20[우변]
// a = 5 (o), 5 = a (x)

// (3) 복합 대입 연산자: 산술 연산자와 대입 연산자의 혼합형
// +=, -=, *=, /=, %=
// a += 10: a에 10을 더한 값을 입력 <=> 의미상 a = a + 10

// (4) 증감 연산자: ++, -- (1을 더하거나 뺄 때)
// (ex) a = 2, a ++ = 3

// (5) 비교 연산자 - 부등호 : >, >=, <, <=, ==, != ('==': 양변이 같으면 true, 아니면 false 출력. '!='는 반대)
// True == True -> True
// True == False -> False
// True != True -> False
// True != False -> True

// (6) 논리 연산자: &&, || ('||'는 Shift + Ent)
// && 는 양변이 모두 True 일 때 True, || 는 한 쪽만 True 여도 True. ! 는 True/False 뒤집음
// True && True -> True
// True || False -> True
// !True -> False
// !False -> True

